public class TempTasking {
    private String taskType;
    private String taskName;
    private String taskAddr;
    private String taskMan;
    private String taskDeadLine;

    public String getTaskType() {
        return taskType;
    }

    public void setTaskType(String taskType) {
        this.taskType = taskType;
    }

    public String getTaskName() {
        return taskName;
    }

    public void setTaskName(String taskName) {
        this.taskName = taskName;
    }

    public String getTaskAddr() {
        return taskAddr;
    }

    public void setTaskAddr(String taskAddr) {
        this.taskAddr = taskAddr;
    }

    public String getTaskMan() {
        return taskMan;
    }

    public void setTaskMan(String taskMan) {
        this.taskMan = taskMan;
    }

    public String getTaskDeadLine() {
        return taskDeadLine;
    }

    public void setTaskDeadLine(String taskDeadLine) {
        this.taskDeadLine = taskDeadLine;
    }
}
